    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        
        if (mowerComboBox.getSelectedIndex() == 0) { //lawntractor mower
            LawnTractor lt = new LawnTractor();
            lt.setManufacturer(manu1TextField.getText());
            lt.setYear(Integer.parseInt(yearTextField.getText()));
            lt.setSerialNumber(snTextField.getText());
            Engine e = new Engine();
            e.setManufacturer(manu2TextField.getText());
            e.setHorsePower(Double.parseDouble(hpTextField.getText()));
            e.setCylinders(Integer.parseInt(cylTextField.getText())); 
            lt.setEngine(e);
            lt.setModel(modelTextField1.getText());
            lt.setDeckWidth(Double.parseDouble(dwTextField.getText()));
            list.addMower(lt);
            list.addLTMower(lt);
        } else if (mowerComboBox.getSelectedIndex() == 1){ //commerical mower
            CommericalMower cm = new CommericalMower();
            cm.setManufacturer(manu1TextField.getText());
            cm.setYear(Integer.parseInt(yearTextField.getText()));
            cm.setSerialNumber(snTextField.getText());
            Engine e = new Engine();
            e.setManufacturer(manu2TextField.getText());
            e.setHorsePower(Double.parseDouble(hpTextField.getText()));
            e.setCylinders(Integer.parseInt(cylTextField.getText())); 
            cm.setEngine(e);
            cm.setModel(modelTextField1.getText());
            cm.setDeckWidth(Double.parseDouble(dwTextField.getText()));
            cm.setOperatingHours(Double.parseDouble(ohTextField.getText()));
            cm.setZeroTurnRadius(Boolean.parseBoolean(ztrTextField.getText()));
            list.addMower(cm);
            list.addCMower(cm);
        } else if (mowerComboBox.getSelectedIndex() == 2) { //Gas Powered Mower
            GasPoweredMower gpm = new GasPoweredMower();
            gpm.setManufacturer(manu1TextField.getText());
            gpm.setYear(Integer.parseInt(yearTextField.getText()));
            gpm.setSerialNumber(snTextField.getText());
            gpm.setCutWidth(Double.parseDouble(dwTextField.getText()));
            gpm.setWheelDiameter(Double.parseDouble(wdTextField.getText()));
            Engine e = new Engine();
            e.setManufacturer(manu2TextField.getText());
            e.setHorsePower(Double.parseDouble(hpTextField.getText()));
            e.setCylinders(Integer.parseInt(cylTextField.getText())); 
            gpm.setEngine(e);
            gpm.setSelfPropelled(Boolean.parseBoolean(spTextField.getText()));
            list.addMower(gpm);
            list.addGSPMower(gpm);
        } else if (mowerComboBox.getSelectedIndex() == 3) { //Push Reel Mower
            PushReelMower pr = new PushReelMower();
            pr.setManufacturer(manu1TextField.getText());
            pr.setYear(Integer.parseInt(yearTextField.getText()));
            pr.setSerialNumber(snTextField.getText());
            pr.setCutWidth(Double.parseDouble(dwTextField.getText()));
            pr.setWheelDiameter(Double.parseDouble(wdTextField.getText()));
            pr.setNumWheels(Integer.parseInt(nwTextField.getText()));
            list.addMower(pr);
            list.addPRMower(pr);
        }
        indicator = 1;
        indicate = "1";
        testArea.setText(indicate);
                
        // TODO add your handling code here:
    }//GEN-LAST:event_addButtonActionPerformed

